'''
 = 简单复制运算
 += 加法赋值运算 a += 1 为 a = a + 1 , 后续类似
 -= 减法赋值运算
 *= 乘法赋值运算
 /= 除法赋值运算
 %= 取模赋值运算
 **= 幂赋值运算
 // 取整除赋值运算符，向下取整
'''
a = 2;
b = 5;
a += b;
print("a += c 为：{}".format(a));
a -= b;
print("a -= b : " , a);
a *= b;
print("a *= b : ", a);
a /= b;
print("a /= b : ", a); 

a **= b;
print("a **= b: ", a);
a //= b;
print("a //= b : ", a);